			<h2 class="style11">Event Structure</h2>
			<p class="style9">

The round consists of 20 - 25 questions and the participants have to decipher the questions one after the other. The duration of the event will be approx 90 minutes which could be extended on the basis of response. If a participant is unable to answer a question he/she will have an option to skip the current question and proceed to the next question but, it can't be reviewed.
</p><br/>
<h2 class="style11">Rules</h2>
<p>
<ul class="style9">
<li>Max. team size is 2</li>
<li>Each Team should register only once specifying their teammates.</li>
<li>Always answer your question with no spaces(if any) in between words and
your answers need to be in "lower case".<br/>
(i.e. if the answer is "Dhirubhai Ambani" write "dhirubhaiambani")</li>
<li>Once you skip a questions you can't return to that later.</li>
<li>For each correct answer your score increases by 10 or by corresponding number if mentioned explicitly.</li>
<li>If the scores are same for two teams ,the time of last answer submission will be considered as the tie-breaker.</li>
<li>Participants from DAIICT  have to register using their student mail ID(Eg:201001049@daiict.ac.in) only. This is mandatory for claiming the prize money.</li>
<li>Any sort of malpractice like multiple accounts shall result in disqualification and coordinators decision shall be considered final.</li>
<li>Teams can login in to the specified webpage any time during the event.</li>
<li>In case of any disputes the decision of the Team  i-decipher would be final.</li>

</ul>
